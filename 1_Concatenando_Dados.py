# 1 - Concatenando Dados 🐾
# Descrição: Vamos receber dois dados diferentes do usuário e concatena-los em uma única string?!

info_1 = input("Digite uma string, por favor: ") # Recebendo a primeira string
info_2 = input("Digite mais uma string, por favor: ") # Recebendo a segunda string

concatenada = info_1 + " " + info_2 # Concatenando as strings
print(concatenada) # Imprimindo o resultado