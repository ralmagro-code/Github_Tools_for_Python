# 3 - Operações Matemáticas Simples
# Descrição: Vamos solicitar como entrada dois números e depois vamos realizar uma operação simples entre eles.

primeiro_numero = float(input("Por favor, digite o primeiro número: ")) # Entrada do primeiro número do usuário
segundo_numero = float(input("Por favor, digite o segundo número: ")) # Entrada do segundo número do usuário

print("A soma dos dois números digitado é {} e o produto deles é {}.".format(primeiro_numero + segundo_numero, primeiro_numero * segundo_numero))