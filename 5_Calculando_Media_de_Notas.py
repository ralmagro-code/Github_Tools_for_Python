# 5 - Calculando Média de Notas
# Descrição: Agora vamos calcular a média de três notas fornecidas na entrada do usuário. Uma dica é: Utilize operadores aritméticos para realizar o cálculo da média.

# Entrada das três notas do usuário
nota1 = float(input("Por favor, digite a primeira nota: "))
nota2 = float(input("Por favor, digite a segunda nota: "))
nota3 = float(input("Por favor, digite a terceira nota: "))

media = (nota1 + nota2 + nota3) / 3 # Cálculo da média das três notas

print("A média das três notas é ", media) # Impressão da média das notas

